class totavg {
    public static void main(String[] args) {
        String s1 = "Deepika";
        String s2 = "Bunty";
        String s3 = "Chintu";
        int a = 90;
        int b = 93;
        int c = 89;
        int sum = a + b + c;
        float avg = (a + b + c) / 3.0f;
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
        System.out.println("Total Marks: " + sum);
        System.out.println("Average Marks: " + avg);
    }
}

